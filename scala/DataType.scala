
object DataType
{
    def main(args : Array[String])
    {
        var flag : Boolean = true;
        var num1 : Int = 22;
        var num2 : Float = 2.345f;
        var num3 : Double = 4.59930495
        var num4 : Char = 'A'

        println(flag)
        println(num1)
        println(num2)
        println(num3)
        println(num4)
    }
}